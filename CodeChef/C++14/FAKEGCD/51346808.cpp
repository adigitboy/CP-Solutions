#include<bits/stdc++.h>
using namespace std;

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    long long t;
    cin>>t;
    while(t--){	
        long long n;
        cin>>n;
        for(int i=1;i<=n;i++){
            cout<<i<<" ";
        }
        cout<<"\n";
    }
    return 0;
}